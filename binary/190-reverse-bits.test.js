const { reverseBits } = require('./190-reverse-bits');

test('n = 00000010100101000001111010011100 returns 964176192 (00111001011110000010100101000000)', () => {
  expect(reverseBits(parseInt('00000010100101000001111010011100', 2))).toBe(
    964176192
  );
});

test('n = 11111111111111111111111111111101 returns 3221225471 (10111111111111111111111111111111)', () => {
  expect(reverseBits(parseInt('11111111111111111111111111111101', 2))).toBe(
    3221225471
  );
});
